package day1;

public class program3_DatatypesExample {

	public static void main(String[] args) {
		// boolean takes 1 bit
		
		boolean result = false;
		System.out.println(result);
		
		// byte takes 1 byte = 8 bits
		// -127 to 128
		
		byte ans = -128;
		System.out.println(ans);
		
		//short take 2 bytes
		// -32000 to 32000
		
		short data = -32000;
		System.out.println(data);
		
		// char takes 2 bytes 
		char alpha = 'A';
		System.out.println(alpha);
		
		// long takes 8 bytes 
		long phonenumber = 1234567892L;
		System.out.println(phonenumber);
		
		
		// Float 
		//Used to store decimal numbers
		// it takes 4 bytes 
		float price = 99.99f;
		System.out.println(price);
		
		
		
		
		
		
		//8 bytes 
		double data3 = 9.9756432564765532;
		System.out.println(data3);
		
		
		
		
		
		
		

	}

}
