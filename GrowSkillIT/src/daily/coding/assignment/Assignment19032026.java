package daily.coding.assignment;

public class Assignment19032026 {

	public static void main(String[] args) {
// Declaring variables with suitable data types
        
        int age = 21;                          // small whole number
        int marks = 450;                       // marks out of 500
        byte battery = 85;                     // 0–100 range
        int cityPopulation = 2500000;          // large but within int range
        char grade = 'A';                      // single character
        int bankBalance = 50000;               // no decimals
        boolean isLoggedIn = true;             // true/false
        int distance = 1200;                   // meters
        long largeNumber = 9000000000L;        // very large number (long)
        
        // Printing output
        
        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Battery: " + battery + "%");
        System.out.println("City Population: " + cityPopulation);
        System.out.println("Grade: " + grade);
        System.out.println("Bank Balance: " + bankBalance);
        System.out.println("Login Status: " + isLoggedIn);
        System.out.println("Distance: " + distance + " meters");
        System.out.println("Large Number: " + largeNumber);

	}

}
